"""
RobinHealth: letter delivery pipeline.

Delivers rendered negotiation letters via the available channels.
Every delivery function returns a DeliveryReceipt -- the same shape
regardless of channel, so callers don't need to branch on channel type.

CHANNELS AND THEIR STATUS:
  email   -- REAL. Uses Python's smtplib (stdlib, no third-party dependency).
             Requires SMTP_HOST (+ optional SMTP_PORT, SMTP_USER, SMTP_PASSWORD,
             SMTP_FROM). Without SMTP_HOST, send_email() returns a DeliveryReceipt
             with status='not_configured' rather than raising.

  fax     -- STUB. Lob and Twilio both offer fax APIs; both blocked from this
             sandbox (confirmed: api.lob.com and api.twilio.com return 403).
             send_fax() is fully structured and will work when LOB_API_KEY or
             TWILIO_ACCOUNT_SID / TWILIO_AUTH_TOKEN are set in production.

  mail    -- STUB. Lob's print-and-mail API is the natural fit (physical letter,
             tracked, USPS). Blocked from this sandbox. send_mail() is structured
             and will work when LOB_API_KEY is set in production.

WHY SMTPLIB FOR EMAIL:
  Sendgrid, Mailgun, Postmark -- all blocked from this sandbox. smtplib works
  with any SMTP relay (Gmail, AWS SES, Postmark SMTP endpoint, self-hosted MTA).
  In production: set SMTP_HOST=smtp.sendgrid.net, SMTP_USER=apikey,
  SMTP_PASSWORD=<sendgrid_api_key> and email delivery works without any
  additional dependency. In dev: leave SMTP_HOST unset and all delivery calls
  return 'not_configured' gracefully.

REFERENCE NUMBERS:
  Every letter gets a reference number (format: RH-{case_id_prefix}-{date}-{seq})
  generated by make_reference_number(). This ties the PDF file, the delivery
  record, and the negotiation_contacts row together.
"""

from __future__ import annotations

import email as _email_module
import os
import smtplib
import uuid
from dataclasses import dataclass
from datetime import date
from email.mime.application import MIMEApplication
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText


# ============================================================
# Dataclasses
# ============================================================

@dataclass
class DeliveryReceipt:
    """
    Result of a delivery attempt, regardless of channel.
    status values: 'sent', 'not_configured', 'failed'
    """
    channel: str
    status: str             # 'sent' | 'not_configured' | 'failed'
    reference_number: str
    recipient_address: str  # email address, fax number, or mailing address
    detail: str             # plain-English description of what happened
    provider_id: str | None = None  # external ID from delivery provider, if any


# ============================================================
# Reference number generation
# ============================================================

def make_reference_number(case_id: str) -> str:
    """
    Generate a human-readable reference number for a letter.
    Format: RH-{first 6 chars of case_id}-{YYYYMMDD}-{random 4 hex chars}

    Example: RH-A1B2C3-20260620-F7D2

    The case_id prefix makes the reference traceable back to the case
    without exposing the full UUID. The date makes it easy to file.
    The random suffix handles same-day multiple letters for one case.
    """
    prefix = case_id.replace("-", "")[:6].upper()
    today = date.today().strftime("%Y%m%d")
    suffix = uuid.uuid4().hex[:4].upper()
    return f"RH-{prefix}-{today}-{suffix}"


# ============================================================
# Email delivery (real)
# ============================================================

def send_email(
    to_address: str,
    subject: str,
    body_text: str,
    pdf_bytes: bytes,
    pdf_filename: str,
    reference_number: str,
    from_name: str = "RobinHealth Patient Advocacy",
) -> DeliveryReceipt:
    """
    Send the letter PDF as an email attachment.

    Configuration (environment variables):
        SMTP_HOST      SMTP server hostname (required; if unset, returns not_configured)
        SMTP_PORT      Port (default: 587 for STARTTLS)
        SMTP_USER      SMTP username (optional; skip auth if unset)
        SMTP_PASSWORD  SMTP password (optional)
        SMTP_FROM      Sender address (default: advocacy@robinhealth.com)
        SMTP_USE_SSL   Set to "true" for SSL on port 465 (default: STARTTLS on 587)

    Works with any SMTP relay:
        AWS SES:     SMTP_HOST=email-smtp.us-east-1.amazonaws.com SMTP_PORT=587
        SendGrid:    SMTP_HOST=smtp.sendgrid.net SMTP_USER=apikey SMTP_PASSWORD=<api_key>
        Gmail:       SMTP_HOST=smtp.gmail.com SMTP_USER=<email> SMTP_PASSWORD=<app_password>
        Postmark:    SMTP_HOST=smtp.postmarkapp.com SMTP_USER=<token> SMTP_PASSWORD=<token>
    """
    smtp_host = os.environ.get("SMTP_HOST")
    if not smtp_host:
        return DeliveryReceipt(
            channel="letter_email",
            status="not_configured",
            reference_number=reference_number,
            recipient_address=to_address,
            detail=(
                "SMTP_HOST is not configured. Set SMTP_HOST (and optionally SMTP_PORT, "
                "SMTP_USER, SMTP_PASSWORD, SMTP_FROM) to enable email delivery."
            ),
        )

    smtp_port = int(os.environ.get("SMTP_PORT", "587"))
    smtp_user = os.environ.get("SMTP_USER")
    smtp_password = os.environ.get("SMTP_PASSWORD")
    smtp_from = os.environ.get("SMTP_FROM", "advocacy@robinhealth.com")
    use_ssl = os.environ.get("SMTP_USE_SSL", "").lower() == "true"

    # Build the message
    msg = MIMEMultipart()
    msg["From"] = f"{from_name} <{smtp_from}>"
    msg["To"] = to_address
    msg["Subject"] = subject
    msg["X-RobinHealth-Reference"] = reference_number

    # Plain-text body
    msg.attach(MIMEText(body_text, "plain"))

    # PDF attachment
    attachment = MIMEApplication(pdf_bytes, _subtype="pdf")
    attachment.add_header(
        "Content-Disposition", "attachment", filename=pdf_filename
    )
    msg.attach(attachment)

    try:
        if use_ssl:
            server = smtplib.SMTP_SSL(smtp_host, smtp_port, timeout=20)
        else:
            server = smtplib.SMTP(smtp_host, smtp_port, timeout=20)
            server.ehlo()
            server.starttls()
            server.ehlo()

        if smtp_user and smtp_password:
            server.login(smtp_user, smtp_password)

        server.sendmail(smtp_from, [to_address], msg.as_string())
        server.quit()

        return DeliveryReceipt(
            channel="letter_email",
            status="sent",
            reference_number=reference_number,
            recipient_address=to_address,
            detail=f"Letter emailed to {to_address} via {smtp_host}:{smtp_port}.",
        )

    except Exception as exc:
        return DeliveryReceipt(
            channel="letter_email",
            status="failed",
            reference_number=reference_number,
            recipient_address=to_address,
            detail=f"Email delivery failed: {type(exc).__name__}: {exc}",
        )


