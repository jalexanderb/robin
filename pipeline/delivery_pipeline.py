# ============================================================

def send_fax(
    fax_number: str,
    pdf_bytes: bytes,
    reference_number: str,
) -> DeliveryReceipt:
    """
    Send the letter PDF by fax.

    Production implementation: Lob (https://lob.com/products/fax) or
    Twilio Programmable Fax (https://www.twilio.com/fax).

    Configuration (Lob):
        LOB_API_KEY   Lob API key

    Configuration (Twilio):
        TWILIO_ACCOUNT_SID
        TWILIO_AUTH_TOKEN
        TWILIO_FAX_FROM   Your Twilio fax number (e.g. +1-888-555-0100)

    Currently returns 'not_configured' because both APIs are blocked from
    this sandbox. Wire up one of these in production:

    Lob example (pip install lob):
        import lob
        lob.api_key = os.environ["LOB_API_KEY"]
        result = lob.Fax.create(to=fax_number, file=pdf_bytes, ...)

    Twilio example (pip install twilio):
        from twilio.rest import Client
        client = Client(account_sid, auth_token)
        fax = client.fax.v1.faxes.create(
            from_=twilio_fax_from, to=fax_number,
            media_url=<publicly accessible URL of the PDF>,
        )
    """
    lob_key = os.environ.get("LOB_API_KEY")
    twilio_sid = os.environ.get("TWILIO_ACCOUNT_SID")

    if not lob_key and not twilio_sid:
        return DeliveryReceipt(
            channel="letter_fax",
            status="not_configured",
            reference_number=reference_number,
            recipient_address=fax_number,
            detail=(
                "Fax delivery requires LOB_API_KEY (Lob) or "
                "TWILIO_ACCOUNT_SID + TWILIO_AUTH_TOKEN + TWILIO_FAX_FROM (Twilio). "
                "Neither is configured. The letter PDF is saved to storage; "
                "download and fax manually, or configure a fax provider."
            ),
        )

    # Production implementation goes here
    # (both API domains blocked in this sandbox -- see module docstring)
    return DeliveryReceipt(
        channel="letter_fax",
        status="not_configured",
        reference_number=reference_number,
        recipient_address=fax_number,
        detail="Fax provider credentials found but delivery not yet implemented in this environment.",
    )


